"""3. Дано перелік значень. Повернути словник, де кожен ключ - це індекс листа,
а значення листа – це значення ключа:
Input:
    ['a', 'b', 'c', 'd', 'e']
Output
    {0: 'a', 1: 'b', 2: 'c', 3: 'd', 4: 'e'}"""

my_list = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'k']
my_dict = {k: v for k, v in enumerate(my_list)}
print(my_dict)
