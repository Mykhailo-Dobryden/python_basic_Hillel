number_x = int(input("Enter number x: "))
number_y = int(input("Enter number y: "))

print('You have entered numbers: ', number_x, ' and ', number_y)

total = number_x + number_y

print("Sum of these numbers is: ", total)

floor_division = number_x // number_y

print("The result of the floor division is: ", floor_division)

remainder = number_x % number_y

print("The remainder is: ", remainder)

x_in_power_y = number_x ** number_y

print(number_x, " in power ", number_y, " is: ", x_in_power_y)
